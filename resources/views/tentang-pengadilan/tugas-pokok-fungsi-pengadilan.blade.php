                                <div class="container-header">
                                    <div class="title-container">
                                        <h2>Tugas Pokok dan Fungsi Pengadilan Militer I-05 Pontianak</h2>
                                    </div>
                                </div>

                                <div class="content">
                                    <br>

                                    <p>Mengenai justisiabel Peradilan Militer berwenang mengadili perkara pidana yang
                                        dilakukan oleh Militer (prajurit TNI) berpangkat Kapten ke bawah yang melakukan
                                        tindak pidana (kejahatan) maupun pelanggaran masih berdinas aktif dan atau
                                        orang-orang yang tunduk pada kekuasaan Peradilan Militer berdasarkan Undang-Undang
                                        Peradilan Militer. Dalam pelaksanaan tugas dan kewenangannya baik itu teknis
                                        yudisial maupun non teknis yudisial Peradilan Militer menetapkan visi dan misi
                                        sesuai dengan kebijakan yang telah digariskan oleh Mahkamah Agung Republik Indonesia
                                        dalam rangka terwujudnya badan peradilan yang ideal.</a></p>
                                    </p>

                                    <p>
                                        Pengadilan Militer I-05 Pontianak di bidang fungsi teknis yudisial bertugas
                                        melaksanakan fungsi kekuasaan kehakiman secara independen, efektif, dan berkeadilan
                                        sesuai dengan peraturan perundang-undangan yang berlaku di Lingkungan Mahkamah Agung
                                        Republik Indonesia dan Mabes TNI. Guna terlaksananya tugas pokok tersebut Pengadilan
                                        Militer I-05 Pontianak menyelenggarakan fungsi utamanya yang meliputi
                                        kegiatan-kegiatan sebagai berikut:
                                    <ol type="a">
                                        <li> Pengadilan Militer I-05 Pontianak sebagai Pengadilan tingkat pertama memeriksa
                                            dan memutus perkara pidana dan pelanggaran (lalu lintas) yang dilakukan oleh
                                            Prajurit TNI berpangkat Kapten ke bawah.</li>
                                        <li>Pengadilan Militer I-05 Pontianak menerima permohonan Banding, Kasasi, PK dan
                                            Grasi dari pihak Terdakwa dan Oditur Militer untuk diteruskan ke Pengadilan
                                            Militer Tinggi I Medan untuk permohonan Banding, dan ke Mahkamah Agung untuk
                                            permohonan Kasasi, PK dan Grasi.</li>
                                    </ol>

                                    </p>
                                    <br>
                                    <p>
                                        Untuk mendukung pelaksanaan bidang fungsi teknis yudisial Pengadilan Militer I-05
                                        Pontianak juga menyelenggarakan fungsi pendukung yaitu bidang non teknis yudisial
                                        yang meliputi kegiatan sebagai berikut :

                                    <p>
                                    <ul>
                                        <li> Manajemen Sumber Daya Manusia. </li>
                                    </ul>
                                    </p>
                                    <p>Pengadilan Militer I-05 Pontianak sebagai pengadilan tingkat pertama
                                        menjalankan manajemen Sumber Daya Manusia meliputi peningkatan
                                        kapabilitas/keahlian setiap personel dengan menyertakan parameter obyektif
                                        sehingga kemampuan personel di semua lini organisasi merata dan berkualitas.
                                        Parameter obyektif ini dirasakan mendesak terutama berkaitan dengan adanya
                                        kebutuhan untuk menerapkan sistem reward and punishment yang tepat sesuai
                                        dengan peraturan yang berlaku.</p>
                                    </ul>
                                    </p>
                                    <p>
                                    <ul>
                                        <li> Manajemen Sumber Daya Keuangan </li>
                                    </ul>
                                    </p>
                                    <p>
                                        Hal ini menjadi salah satu faktoreksternal yang sangat perlu diperhitungkan dan
                                        dicermati dalam pelaksanaan fungsi teknis yustisial. Pelaksanaan penerimaan dan
                                        belanja negara (realisasi anggaran) didasarkan Standard Operating Procedure (SOP)
                                        yang telah ditetapkan. Walaupun terdapat sedikit hambatan internal diantaranya
                                        adanya perangkapan jabatan antara jabatan struktural dengan jabatan pengelola
                                        keuangan seperti Kuasa Pengguna Anggaran oleh Sekretaris, PPSPM oleh Kasubbag Umum
                                        dan Keuangan sehingga fungsi pengelola keuangan menjadi second schedule setelah
                                        melaksanakan tugas pokoknya sehari-hari.
                                    </p>
                                    <p>
                                    <ul>
                                        <li> Manajemen Sarana dan Prasarana </li>
                                    </ul>
                                    </p>
                                    <p>
                                        Unsur penunjang lain yang pentingdalam mewujudkan visi dan misi Pengadilan Militer
                                        I-05 Pontianak adalah kemampuan mengelola sarana dan prasarana dalam rangka
                                        mendukung lingkungan kerja yang aman, nyaman, dan kondusif bagi penyelenggaraan
                                        peradilan. Hal itu mencakup pengelolaan barang milik negara yang biasa dikenal
                                        dengan manajemen aset milik negara. Sebagaimana ditentukan dalam Peraturan
                                        Pemerintah Nomor 6 Tahun 2006 tentang Pengelolaan Barang Milik Negara atau Daerah,
                                        cakupan manajemen aset milik negara meliputi :
                                    <ol>
                                        <li>Perencanaan kebutuhan dan penganggaran</li>
                                        <li>Pengadaan</li>
                                        <li>Penggunaan</li>
                                        <li>Pemanfaatan</li>
                                        <li>Pengamanan dan pemeliharaan</li>
                                        <li>Penilaian</li>
                                        <li>Penghapusan</li>
                                        <li>Pemindahtanganan</li>
                                        <li>Penatausahaan</li>
                                        <li>Pembinaan, pengawasan, dan pengendalian.</li>
                                    </ol>
                                    </p>

                                    <p>
                                        Apabila kesepuluh bagian tersebut disederhanakan dan digambarkan, maka akan
                                        diperoleh gambaran siklus hidup aset (life-cycle costing) sebagai berikut :
                                    <ol>
                                        <li>Fase perencanaan, yaitu identifikasi kebutuhan ketika terdapat permintaan
                                            kebutuhan atas aset akibat perencanaan.</li>
                                        <li>Fase pengadaan, yaitu ketika aset dibeli, dibangun atau dibuat.</li>
                                        <li>Fase pengoperasian dan pemeliharaan, yaitu ketika aset digunakan untuk tujuan
                                            yang telah ditentukan. Fase ini mungkin diselingi dengan pembaruan atau
                                            perbaikan besar-besaran secara periodik serta penggantian atas aset yang rusak
                                            dalam periode penggunaan.</li>
                                        <li>Fase penghapusan (disposal), yaitu ketika umur ekonomis suatu aset telah habis
                                            atau ketika kebutuhan atas pelayanan yang disediakan aset telah hilang.</li>
                                    </ol>
                                    </p>

                                    <p>
                                    <ul>
                                        <li> Manajemen Teknologi dan Informasi (TI). </li>
                                    </ul>
                                    </p>
                                    <p>Pengadilan Militer I-05 Pontianak telah berupaya untuk mengaplikasikan teknologi
                                        dalam pengelolaan informasi yang diperlukan internal organisasi maupun para pencari
                                        keadilan dan pengguna layanan pengadilan. Namun demikian perlu adanya satu kebijakan
                                        sistem pengelolaan teknologi informasi yang komprehensif dan terintegrasi, untuk
                                        memudahkan dan mempercepat proses pelaksanaan tugas dan fungsi di setiap unit kerja.
                                        Dengan demikian dapat diharapkan terjadinya peningkatan penyelesaian administrasi
                                        perkara (minutasi) dan kualitas pelayanan informasi kepada masyarakat.</p>

                                    <p>
                                    <ul>
                                        <li> Transparansi Peradilan. </li>
                                    </ul>
                                    </p>
                                    <p>Pengadilan Militer I-05 Pontianak sudah melaksanakan keterbukaan terhadap akses
                                        informasi, diantaranya telah tersedia informasi melalui website Pengadilan Militer
                                        I-05 Pontianak dengan alamat dilmil-pontianak.go.id, facebook
                                        (pengadilanmiliter.pontianak), Instagram (pengadilan_militer_ 105_pontianak) dan
                                        youtube resmi Pengadilan Militer I-05 Pontianak, informasi perkara melalui Sistem
                                        Informasi Penelusuran Perkara (SIPP) Web dengan alamat sipp.dilmil-pontianak.go.id,
                                        perpustakaan online (perpustakaan.dilmil-pontianak.go.id) tersedianya fasilitas TV
                                        media, komputer informasi atau kios touchscreen, Aplikasi buku tamu elektronik,
                                        papan pengumuman, brosur-brosur, banner informasi, dan di lengkapi dengan meja
                                        Pelayanan Terpadu Satu Pintu (PTSP), dan terdapat pula Aplikasi Sipopmil (Sistem
                                        Informasi Pelayanan Online Pengadilan Militer) berbasis whatsaap businees dan
                                        aplikasi unggulan Sipopmil Prima (Sistem Informasi Pelayanan Online Pengadilan
                                        Militer Prima) aplikasi tersebut berbasis android yang bisa di download pada laman
                                        google play store. Aplikasi Sipopmil Prima merupakan upgrade dari aplikasi Sipopmil
                                        yang dikembangkan oleh SDM TI Pengadilan Militer I-05 Pontianak. Adapun menu dalam
                                        aplikasi Sipopmil Prima tersebut adalah sebagai berikut :</p>
                                    <ol>
                                        <li>Website (Menyajikan Berita dan Informasi tentang Pengadilan secara lengkap)</li>
                                        <li>SIPP (Sistem Informasi Penelusuran Perkara)</li>
                                        <li>Jadwal Sidang</li>
                                        <li>Survey Online (Survei Indeks Persepsi Korupsi dan Survey Indeks Kepuasan
                                            Masyarakat)</li>
                                        <li>Perpustakaan Online</li>
                                        <li>Pengaduan</li>
                                        <li>Absensi Online</li>
                                        <li>Sipopmil (Sistem Informasi Pelayanan Online Pengadilan Militer berbasis WA
                                            Business)</li>
                                        <li>Direktori Putusan</li>
                                        <li>Instagram</li>
                                        <li>Facebook</li>
                                        <li>Channel Youtube</li>
                                    </ol>

                                    <p>Dengan aplikasi tersebut mempermudah pengguna layanan dalam memperoleh informasi
                                        pengadilan secara online di Pengadilan Militer I-05 Pontianak.
                                        Mekanisme penyediaan dan penyimpanan informasi juga perlu terus ditingkatkan
                                        sehingga Pengadilan Militer I-05 Pontianak selalu siap dalam melayani dan merespon
                                        permintaan informasi oleh pengguna layanan Pengadilan.</p>

                                    <p>
                                    <ul>
                                        <li> Fungsi Pengawasan </li>
                                    </ul>
                                    </p>

                                    <p>Dalam pelaksanaan pengawasan baik fungsiteknis yudisial maupun fungsi pendukung non
                                        teknis yudisial dalam program kerja Pengadilan Militer I-05 Pontianak tahun Anggaran
                                        2024 melakukan pengawasan internal secara langsung maupun tidak langsung yaitu :</p>
                                    <ol>
                                        <li>Kegiatan pengawasan secara langsung dilaksanakan oleh kepala sub bagian dan
                                            Panitera Muda yang mengawasi jalannya tugas dan tanggungjawab sesuai tugas pokok
                                            masing-masing bidang, yang dikoordinasikan oleh kepala sub bagian atau Panitera
                                            Muda kepada Sekretaris dan Panitera kemudian dilaporkan pelaksanaannya kepada
                                            Kadilmil.</li>
                                        <li>Pengawasan secara tidak langsung dilakukan oleh Hakim Pengawas Bidang yang
                                            terdiri dari 4 (empat) bidang yakni :</li>
                                        <ol type = "a">
                                            <li>Hakim Pengawas Bidang Hukum dan Bidang Pidana.</li>
                                            <li>Hakim Pengawas Bidang Umum dan Keuangan, dan Pelayanan Terpadu Satu Pintu
                                                (PTSP).</li>
                                            <li>Hakim Pengawas Bidang Kepegawaian dan Ortala.</li>
                                            <li>Hakim Pengawas Bidang Perencanaan, TI dan Pelaporan.</li>
                                        </ol>
                                    </ol>
                                    <p>Dalam pelaksanaannya Hakim Pengawas Bidang berkoordinasi dengan kepala sub bagian dan
                                        hasil pengawasan dikoordinir oleh Koordinator Hakim Pengawas (Wakadilmil), apabila
                                        jabatan Wakadilmil kosong Koordinator Hakim Pengawas Bidang di rangkap oleh salah
                                        satu Kimwasbid kemudian laporan Koordinator Hakim Pengawas Bidang dilaporkan kepada
                                        Kepala Pengadilan.</p>

